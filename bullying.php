			<p><h3><b>How to prevent Cyberbullying?</b></h3></p>
			<ul>
			<li>Make sure you use Privacy Settings.
			Many social media websites (Facebook, Twitter, Instagram, etc…) have options so that you
			can hide your personal details from strangers. These can be found in the settings menu of
			the website and it is important that you activate these to stay safe online.</li>
			<li>Always respect others - be careful what you say online.
			Anything written online can be tracked back to the person who wrote it. Make sure that
			what you have written online is not harmful to others. Treats others how you want to be
			treated!</li>
			<li>Be careful what pictures or videos you upload. Once a picture is shared online it cannot be taken
			down.</li>
			 Only upload photos or videos to the internet that your parent(s) or guardian have approved.
			<li>Only add people you know and trust to friends/followers lists online. When talking to strangers,
			keep your personal information safe and location hidden.
			Never add people that you do not know, these people can sometimes be dangerous towards
			you and your friends. If you are unsure about a friend request, speak to a parent(s) or
			guardian.</li>
			<li>Block the bully - learn how to block or report someone who is behaving badly.
			Many social media websites (Facebook, Twitter, Instagram, etc…) have options so that you
			block certain people from either contacting you or seeing your profile. It is also important to
			tell a parent, teacher or guardian of any bullying you have experienced.</li>
			<li>Do not retaliate or reply to offending e-mails, text messages or online conversations.
			Sometimes when someone upsets us, it’s easy to respond without thinking and you may also
			be cyberbullying as a result. If someone says something which upsets you online, block the 
			person and tell a parent(s) or guardian straight away. If the person bullying you is at the
			same school as you, tell a teacher as well</li>
			<li>Save the evidence. Always keep a copy of offending e-mails, text messages or a screen grab of
			online conversations and pass to a parent, a carer or a teacher.</li>
			<li>Make sure you tell an adult you trust, for example, a parent, a guardian, a teacher, or the antibullying
			co-ordinator or call a helpline like Childline on 08001111 in confidence.
			Telling a person about cyberbullying can help stop the problem straight away before any
			more harm is caused</li>
			<li>Most social media services and other sites have a button you can click on to report bullying. Doing
			this can prevent a bully from targeting you and others in the future. Many services take bullying
			seriously and will either warn the person or delete his or her account.</li>
			</ul>
