			<p>Cyberbullying is where an individual or group of people use a computer, phone or tablet to target
			and purposely upset somebody else.</p>
			<p>Any cyberbullying is wrong and in extreme cases is against the law. The laws which can be broken
			through cyberbullying are:</p>
			<ul style="list-style-type:none">
			<li>Protection from Harassment Act (1997),</li>
			<li>Malicious Communications Act (1988),</li>
